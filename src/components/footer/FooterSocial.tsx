export function FooterSocial({
  variant = 'icons',
  links = defaultLinks,
  className,
}: FooterSocialProps) {
  // Inline variant (with labels)
  if (variant === 'inline') {
    return (
      <div className={cn('flex flex-wrap items-center gap-4', className)}>
        {links.map((link) => (
          <a
            key={link.name}
            href={link.href}
            target="_blank"
            rel="noopener noreferrer"
            className="flex items-center gap-2 text-sm text-zinc-400 hover:text-white transition-colors"
          >
            {link.icon}
            <span>{link.name}</span>
          </a>
        ))}
      </div>
    )
  }

  // Buttons variant
  if (variant === 'buttons') {
    return (
      <div className={cn('flex flex-wrap gap-2', className)}>
        {links.map((link) => (
          <a
            key={link.name}
            href={link.href}
            target="_blank"
            rel="noopener noreferrer"
            className="flex items-center gap-2 px-4 py-2 rounded-lg bg-zinc-800 text-zinc-300 hover:bg-zinc-700 hover:text-white transition-colors"
          >
            {link.icon}
            <span className="text-sm">{link.name}</span>
          </a>
        ))}
      </div>
    )
  }

  // Icons variant (default)
  return (
    <div className={cn('flex items-center gap-3', className)}>
      {links.map((link) => (
        <a
          key={link.name}
          href={link.href}
          target="_blank"
          rel="noopener noreferrer"
          className="p-2 rounded-lg text-zinc-400 hover:text-white hover:bg-zinc-800 transition-colors"
          aria-label={link.name}
        >
          {link.icon}
        </a>
      ))}
    </div>
  )
}

export default FooterSocial
